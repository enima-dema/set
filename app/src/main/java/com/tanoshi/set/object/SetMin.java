package com.tanoshi.set.object;

public class SetMin {
	CardMin one, two, three;

	public SetMin(CardMin one, CardMin two, CardMin three) {
		super();
		this.one = one;
		this.two = two;
		this.three = three;
	}

	public CardMin getOne() {
		return one;
	}

	public void setOne(CardMin one) {
		this.one = one;
	}

	public CardMin getTwo() {
		return two;
	}

	public void setTwo(CardMin two) {
		this.two = two;
	}

	public CardMin getThree() {
		return three;
	}

	public void setThree(CardMin three) {
		this.three = three;
	}
	
	
}
